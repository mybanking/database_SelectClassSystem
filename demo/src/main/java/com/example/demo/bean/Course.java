package com.example.demo.bean;


public class Course {

  private String courseId;
  private String courseOrder;
  private String courseName;
  private long courseMemberNum;
  private long courseTotalTimes;
  private long credit;
  private String semester;
  private String type;
  private String limitGrade;
  private String time;
  private String language;
  private String year;
  private String weekdata;
  private String teacherName;


  public String getCourseId() {
    return courseId;
  }

  public void setCourseId(String courseId) {
    this.courseId = courseId;
  }


  public String getCourseOrder() {
    return courseOrder;
  }

  public void setCourseOrder(String courseOrder) {
    this.courseOrder = courseOrder;
  }


  public String getCourseName() {
    return courseName;
  }

  public void setCourseName(String courseName) {
    this.courseName = courseName;
  }


  public long getCourseMemberNum() {
    return courseMemberNum;
  }

  public void setCourseMemberNum(long courseMemberNum) {
    this.courseMemberNum = courseMemberNum;
  }


  public long getCourseTotalTimes() {
    return courseTotalTimes;
  }

  public void setCourseTotalTimes(long courseTotalTimes) {
    this.courseTotalTimes = courseTotalTimes;
  }


  public long getCredit() {
    return credit;
  }

  public void setCredit(long credit) {
    this.credit = credit;
  }


  public String getSemester() {
    return semester;
  }

  public void setSemester(String semester) {
    this.semester = semester;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public String getLimitGrade() {
    return limitGrade;
  }

  public void setLimitGrade(String limitGrade) {
    this.limitGrade = limitGrade;
  }


  public String getTime() {
    return time;
  }

  public void setTime(String time) {
    this.time = time;
  }


  public String getLanguage() {
    return language;
  }

  public void setLanguage(String language) {
    this.language = language;
  }


  public String getYear() {
    return year;
  }

  public void setYear(String year) {
    this.year = year;
  }


  public String getWeekdata() {
    return weekdata;
  }

  public void setWeekdata(String weekdata) {
    this.weekdata = weekdata;
  }


  public String getTeacherName() {
    return teacherName;
  }

  public void setTeacherName(String teacherName) {
    this.teacherName = teacherName;
  }

}
